                <h3>What Is An Aurora?</h3>
                <p>Charged particles are thrown off by the sun and can intersect with the Earth's magnetic field. These particles can be drawn down to the magnetic poles. Being so energetic, they cause molecules in our atmosphere to glow like a neon sign - this is the aurora</p>
				
				<h3>Where is the best place to see one?</h3>

<p>The perfect site is nice and dark, and has a good unobstructed view of the southern sky. A dark site will preserve your night vision and make photography easier too. Your view south should have no artificial lighting.

<p>In reality most of us have to contend with light pollution in one form or another. As much as possible you want to minimise artificial lighting to the south south. You also want to avoid bright lights shining onto your eyes and camera.



<h3>What does an aurora look like?</h3>

<p>Depending on the brightness of the display, it can range from smokey white, to vivid greens, reds, and purples. It can hug the southern horizon as a minor display or swirl overhead in a major storm.

<p>For most people there is the impression of a ghostly green glow in the sky. This impression can be intensified when it shines through a hole in the clouds

<p>Some tips to maximise your chances of seeing the aurora include:

<ul>
<li>Use “averted vision”. Instead of staring at the aurora, try looking slightly to the side, as though you are trying to catch it out of the corner of your eye. This allows you to use the more sensitive part of your retina, and you may even pick up colours this way.</li>

<li>Let your eyes get “dark adapted”. Bright light naturally depletes a pigment in your retina used in vision. Darkness allows this pigment to build up to high levels. This process can take up to 30-40 minutes. If you need a torch to see what is happening in the dark, use a dim, red coloured light, as it will preserve your night-vision.</li>

<li>In most cities and towns, enough light gets thrown up into the sky to make clouds appear brighter than the starry sky. An aurora will silhouette clouds if it is present.</li>
</ul>


			
